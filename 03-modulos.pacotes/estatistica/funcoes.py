def media( valores):
    """
    Calcula a média dos valores de uma lista
    """
    return sum(valores) / len(valores)


def minimo( valores):
    """
    Retorna o menor valor de uma lista
    """
    return min(valores)


def maximo( valores):
    """
    Retorna o maior valor de uma lista
    """
    return max(valores)